SELECT
    'PINBANK' as gateway,
    CASE
        WHEN datahora REGEXP '^[0-9]{4}-[0-9]{2}-[0-9]{2}T[0-9]{2}:[0-9]{2}:[0-9]{2}' THEN
            STR_TO_DATE(SUBSTRING(datahora, 1, 19), '%Y-%m-%dT%H:%i:%s')
        WHEN datahora REGEXP '^[0-9]{4}-[0-9]{2}-[0-9]{2} [0-9]{2}:[0-9]{2}:[0-9]{2}' THEN
            STR_TO_DATE(datahora, '%Y-%m-%d %H:%i:%s')
        ELSE
            NOW()
    END as datahora,
