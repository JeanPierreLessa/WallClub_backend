    nsuAcquirer,
    CAST(nsuPinbank AS CHAR) as nsu_gateway,
    arqc,
    aid,
    terminalTimestamp,
    captureType,
    hostTimestampCancellation,
    authorizationCode,
    CAST(nsuHost AS CHAR) as nsuHost,
    applicationName,
    brand,
    paymentMethod,
    totalInstallments,
    nsuTerminalCancellation,
    billPaymentEffectiveDate,
    pinCaptured,
    CAST(hostTimestamp AS CHAR) as hostTimestamp,
    capturedTransaction,
    cardName,
    cardNumber,
    operador_pos,
    valor_desconto,
    valor_cashback,
    autorizacao_id,
    cashback_concedido,
    cupom_id,
    cupom_valor_desconto,
    modalidade_wall,
    NOW() as created_at,
    NOW() as updated_at
FROM wclub.transactiondata
WHERE nsuPinbank IS NOT NULL
ORDER BY id;
